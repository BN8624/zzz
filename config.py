import os
from dotenv import load_dotenv

# .env 파일(API 키 등 비밀값이 담긴 파일)을 읽어옵니다.
# .env 는 GitHub 에 올리지 않고, 서버에서 직접 만듭니다.
load_dotenv()


# -------------------------------------------------------------
#  1) API 키  ―  실제 키 값은 여기 적지 않습니다!
#     .env 파일에서 읽어옵니다. (코드 노출돼도 키는 안전)
# -------------------------------------------------------------
GOOGLE_API_KEY = os.getenv("GOOGLE_API_KEY")


# -------------------------------------------------------------
#  2) 사용할 모델 이름 (Google AI Studio 기준)
#     - 26B: 지시 / 디버깅 / 주제생성  담당
#     - 31B: 코딩 / 검토 / 평가        담당
#     두 모델이 역할을 3개씩 나눠 가져서 사용량이 균형을 이룹니다.
# -------------------------------------------------------------
MODELS = {
    "gemma_26b": "gemma-4-26b-a4b-it",   # 26B 모델 ID
    "gemma_31b": "gemma-4-31b-it",       # 31B 모델 ID
    "embedding": "gemini-embedding-001", # 오답노트 의미검색용 임베딩
}


# -------------------------------------------------------------
#  3) 호출 속도 제한 (Rate Limit)
#     모델을 너무 빠르게 연속 호출하면 분당 한도(RPM)에 걸립니다.
#     그래서 호출과 호출 사이에 최소 몇 초를 강제로 쉽니다.
# -------------------------------------------------------------
RATE_LIMIT = {
    "min_interval_seconds": 6,   # 호출 사이 최소 6초 대기
}


# -------------------------------------------------------------
#  4) 하루 작업량 상한
#     RPD(하루 호출 한도)를 넘지 않도록 작업 수를 제한합니다.
#     질 우선 정책이라 무리하게 많이 돌리지 않습니다.
# -------------------------------------------------------------
DAILY_LIMIT = {
    "max_tasks_per_day": 200,    # 하루 최대 작업 개수
}


# -------------------------------------------------------------
#  5) 디버깅 반복 횟수
#     코딩 → AST → 디버깅 루프를 최대 몇 번 돌릴지.
#     숫자가 클수록 코드를 더 다듬습니다(질↑, 호출수↑).
#     "양보다 질" 정책이라 5로 둡니다.
# -------------------------------------------------------------
MAX_DEBUG_ITERATIONS = 5


# -------------------------------------------------------------
#  6) 결과 저장 정책
#     AI 평점이 일정 점수 이상일 때만 DB에 저장하고,
#     점수에 따라 보관 기간(며칠 뒤 자동 삭제)을 다르게 둡니다.
#     ⭐(즐겨찾기) 표시한 것은 영구 보관됩니다.
# -------------------------------------------------------------
DATA_RETENTION = {
    "min_score_to_save": 4.0,    # 4.0점 미만은 저장 안 함(폐기)
    "auto_delete_days": {
        4.0: 7,    # 4.0점 → 7일 보관
        4.5: 14,   # 4.5점 → 14일 보관
        5.0: 30,   # 5.0점 → 30일 보관
    },
}


# -------------------------------------------------------------
#  7) 자동 실행 스케줄
#     작업 사이 대기 시간. 질 우선이라 느긋하게 둡니다.
#     600초 = 10분마다 1작업.
# -------------------------------------------------------------
SCHEDULING = {
    "interval_between_tasks_seconds": 600,   # 작업 간 10분 대기
    "cleanup_hour": 3,                       # 새벽 3시에 자동 정리
}


